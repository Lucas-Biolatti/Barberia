    public void armarCabecera(){
    List<Object> cabecera = new ArrayList<>();
    cabecera.add("Codigo");
    cabecera.add("Descripcion");
    cabecera.add("Stock Min");
    cabecera.add("STOCK");
    cabecera.add("Precio");
    
    for(Object o:cabecera){
        tabla.addColumn(o);
    }
    tblProductos.setModel(tabla);
    }
    
    public void borrarFilas(){
    try{
        int a = tabla.getRowCount()-1;
       
        for(int i=a;i>=0;i--){
            tabla.removeRow(i);
            
        }
    }catch(Exception e){
        JOptionPane.showMessageDialog(this, "La Categoria no tiene productos");
    }
    }
      
    public void cargarStock(){
        
        String categoria = (String) cbxCategorias.getSelectedItem();
        List<Producto> productos = pd.obtenerProductos(categoria);
        for(Producto p : productos){
            tabla.addRow(new Object[]{p.getIdProducto(),p.getDescripcion(),p.getStockMin(),p.getStock(),p.getPrecioUnitario()});
        }
    
    }
    
